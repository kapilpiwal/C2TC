package core_java_practice;

public class Switch2 {

	public static void main(String[] args) {
		// switch case in java (Char)
		char a = 'X';
		switch (a)
		{
		case 'A': System.out.println("The value of a is A"); break;
		case 'B': System.out.println("The value of a is B"); break;
		default: System.out.println("The value of a is other than A & B"); break;
		}

	}

}
