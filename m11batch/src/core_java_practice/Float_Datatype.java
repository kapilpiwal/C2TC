package core_java_practice;

class Float_Datatype
{
	public static void main(String[] args)
	{
		//primitive datatype - float
		
		int v1 = 16/3;
		float v2 = 10f/4f;
		double v3 = 10d/6d;
		
		System.out.println("Value 1 = "+v1);
		System.out.println("Value 2 = "+v2);
		System.out.println("Value 3 = "+v3);
		
		
		int marks = 512;
		double percentage = marks * 0.46f ;
		
		System.out.println("\nPercentage : "+percentage);
	}
}
