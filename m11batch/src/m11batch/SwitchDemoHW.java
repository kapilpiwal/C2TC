package m11batch;
import java.util.*;
public class SwitchDemoHW {

	public static void main(String[] args) {
		int x=10;
		switch(x)
		{
		case 'a':
			System.out.println("a");
			break;
			
		case 97:
			System.out.println("97");
			break;
			
			default: System.out.println("default statement");
		}

	}

}
