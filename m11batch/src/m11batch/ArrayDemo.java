package m11batch;

public class ArrayDemo {

	public static void main(String[] args) {
		//date 14 march 2022
		int[][] x=new int[2][3];
		System.out.println(x);
		
		System.out.println(x[0]);
		
		System.out.println(x[0][0]);
		
		System.out.println(x[0][1]);
		
		System.out.println(x[1][0]);
		
		System.out.println(x[1][1]);

	}

}
